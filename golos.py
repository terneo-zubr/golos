from gtts import gTTS

# Ваш текст
text = ("Ви зателефонували в техпідтримку ДС Електронікс. Якщо у вас питання щодо стану ремонту пристрою, "
        "який вже відправлений в Сервісний центр, натисніть 1. Якщо ваше питання технічного характеру, натисніть 2.")

# Создаём аудиофайл
tts = gTTS(text, lang="uk")
tts.save("output.mp3")

print("Аудиофайл сохранён как 'output.mp3'")
